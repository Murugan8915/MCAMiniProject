def generate_data_dictionary():
    return """
### 4.5 DATABASE DATA DICTIONARY:
The system uses a non-relational MongoDB database. Below is the detailed schema for every collection.

#### 4.5.1 USER COLLECTION (UserModel):
- **_id**: Primary key (ObjectId). Unique identifier for each user.
- **name**: String. Full name of the candidate or staff member.
- **email**: EmailStr. Unique email address used for authentication.
- **username**: String. Optional username for login flexibility.
- **hashed_password**: String. Securely hashed password using the bcrypt algorithm.
- **mobile_number**: String. Contact number for SMS or call notifications.
- **role**: String. Can be "HR", "Candidate", or "Technical Panel". This determines the dashboard permissions.
- **created_at**: DateTime. Timestamp of account registration.

#### 4.5.2 JOB COLLECTION (JobModel):
- **_id**: Primary key (ObjectId). Unique identifier for each job vacancy.
- **title**: String. Designation of the role (e.g., Senior Python Developer).
- **description**: String. Comprehensive job description and responsibilities.
- **required_skills**: List of Strings. List of tags used for AI matching.
- **last_date**: DateTime. Deadline for applications.
- **vacancies**: Integer. Number of positions available.
- **created_by**: String. Email of the HR manager who posted the job.
- **status**: String. "Active", "Deadline Passed", or "Closed".

#### 4.5.3 APPLICATION COLLECTION (ApplicationModel):
- **_id**: Primary key (ObjectId).
- **job_id**: String. Foreign key linking to the Job collection.
- **candidate_email**: String. Foreign key linking to the User collection.
- **resume_file_id**: String. Pointer to the PDF file stored in GridFS.
- **status**: String. "Applied", "Shortlisted", "Rejected", "Technical Assigned", or "Final Selected".
- **ai_score**: Float. Score from 0 to 100 generated by Llama3.
- **ai_feedback**: String. Textual summary from the AI model.

#### 4.5.4 QUESTION COLLECTION (QuestionModel):
- **_id**: Primary key.
- **job_id**: String. Link to specific job role.
- **question_text**: String. The technical question.
- **options**: List of Strings. For MCQ based questions.
- **correct_answer**: String. The expected response.

#### 4.5.5 ANSWER COLLECTION (AnswerModel):
- **application_id**: String. Link to the specific candidate application.
- **question_id**: String. Link to the technical question.
- **answer_text**: String. The candidate's response.
- **score**: Integer. Score assigned by the Technical Panel.
- **feedback**: String. Comments from the technical interviewer.
"""

def generate_api_docs():
    return """
### 4.6 API ENDPOINT DOCUMENTATION:
The backend is built with FastAPI and exposes the following RESTful endpoints.

#### 4.6.1 AUTHENTICATION ENDPOINTS:
- **POST /auth/register**: Registers a new user and hashes their password.
- **POST /auth/login**: Validates credentials and returns a session/token.
- **GET /auth/me**: Returns the profile of the currently logged-in user.

#### 4.6.2 JOB MANAGEMENT ENDPOINTS:
- **POST /jobs/**: (HR Only) Creates a new job posting.
- **GET /jobs/**: Returns a list of all active jobs.
- **GET /jobs/{id}**: Returns details of a specific job.
- **POST /jobs/{id}/lock**: (HR Only) Triggers the AI shortlisting process.

#### 4.6.3 APPLICATION ENDPOINTS:
- **POST /applications/apply**: (Candidate Only) Submits a resume for a job.
- **GET /applications/my-applications**: Returns status of candidate's applications.
- **GET /applications/job/{job_id}**: (HR Only) Returns all applicants for a specific job.

#### 4.6.4 TECHNICAL PANEL ENDPOINTS:
- **POST /technical/assign**: (HR Only) Assigns technical questions to a candidate.
- **POST /technical/evaluate**: (Tech Panel Only) Scores a candidate's technical answers.
"""

